/**
 ******************************************************************************
 ** \brief  Main function of project
 **
 ** \return uint32_t return value, if needed
 **
 ** 将SWD端口配置成通用GPIO功能
 **
 ******************************************************************************/
int32_t main(void)
{
    uint32_t tmp32;

    //================================================================================
    //复位后延时2秒再将SWD接口变成GPIO，防止芯片被锁死
    InitTick(8000000); //SYSTICK 的工作频率为8MHz，每ms中断一次
    SysTickDelay(2000);

    //================================================================================
    //Open GPIOA Clk
    CW_SYSCTRL->AHBEN_f.GPIOA = 1;
    //将PA02 / PA05 配置成通用GPIO功能
    GPIO_SWD2GPIO();
    //配置 PA02 / PA05 为数字功能 - 输出
    REGBITS_CLR(CW_GPIOA->ANALOG, bv2 | bv5);
    REGBITS_CLR(CW_GPIOA->DIR, bv2 | bv5);

    tmp32 = GetTick() + 10000;
    while (GetTick() < tmp32) //在10秒内为GPIO功能
    {
        PA02_SETHIGH();
        PA05_SETHIGH();
        PA05_SETLOW();
        PA02_SETLOW();
        PA05_SETHIGH();
        PA05_SETLOW();
    }

    //================================================================================
    //配置 PA02 / PA05 为UART1.RXD / UART1.TXD
    REGBITS_CLR(CW_GPIOA->ANALOG, bv2 | bv5);         //设置PA02/PA05为数字功能
    REGBITS_MODIFY(CW_GPIOA->DIR, bv2 | bv5, bv2);    //设置PA02为输入，PA05为输出
    PA02_AFx_UART1RXD();                              //配置PA02为UART1.RXD
    PA05_AFx_UART1TXD();                              //配置PA05为UART1.TXD

    //================================================================================

    while (1)
    {
        ;
    }

    //================================================================================
}

